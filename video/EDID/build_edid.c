void edid_prepare(unsigned char *new_edid)
{
    unsigned char i, j;
    unsigned char *ptr;
    unsigned char len;
    ADV76XX_INPUT_TYPE mInput;

    for(i = 54; i < 125; i += 18) {
        ptr = &new_edid[i];
        int width = ptr[2] + (ptr[4] >> 4) * 256;
        int height = ptr[5] + (ptr[7] >> 4) * 256;
        int interlaced = ptr[17] & 0x80;
        printf("width %d height %d interlaced %d cur type %d\n",
               width, height, interlaced, mInput);

        if(interlaced) {
            if(width * 9 == height * 16 * 2) {
                printf("16:9\n");

                if(width >= 1920)
                    for(j = 0; j < 18; j++)
                        ptr[j] = dtd_1920x1080[j];

                else
                    for(j = 0; j < 18; j++)
                        ptr[j] = dtd_1280x720[j];

            } else if(width * 3 == height * 4 * 2) {
                printf("4:3\n");

                for(j = 0; j < 18; j++)
                    ptr[j] = dtd_1024x768[j];

            } else if(width * 10 == height * 16 * 2) {
                printf("16:10\n");

                for(j = 0; j < 18; j++)
                    ptr[j] = dtd_1280x800[j];
            }
        }

        /* Detailed Timing Information */
        if(((ptr[1] * 256 + ptr[0]) / 100) > 165) {
            /* DTD Lists mode that is with PCLK > 165 MHz */
            printf("width %d height %d\n", width, height);

            if(width * 9 == height * 16) {
                printf("16:9\n");

                if(width >= 1920) {
                    for(j = 0; j < 18; j++)
                        ptr[j] = dtd_1920x1080[j];

                } else {
                    for(j = 0; j < 18; j++)
                        ptr[j] = dtd_1280x720[j];
                }

            } else if(width * 3 == height * 4) {
                printf("4:3\n");

                for(j = 0; j < 18; j++)
                    ptr[j] = dtd_1024x768[j];

            } else if(width * 10 == height * 16) {
                printf("16:10\n");

                for(j = 0; j < 18; j++)
                    ptr[j] = dtd_1280x800[j];

            } else {
                if(width >= 1920) {
                    for(j = 0; j < 18; j++)
                        ptr[j] = dtd_1920x1080[j];

                } else {
                    for(j = 0; j < 18; j++)
                        ptr[j] = dtd_1280x720[j];
                }
            }

        } else {
            if((ptr[0] == 0 && ptr[1] == 0) && (ptr[3] == 253)) {
                // Monitor Range Descriptor
                ptr[9] = miner(ptr[9], 16); // Minumum of: monitor's max pclk,165MHz
            }
        }
    }

    new_edid[127] = calculate_checksum(new_edid);

    if(new_edid[126] == 0)
        return;

    printf("prepare extend edid\n");
    ptr = &new_edid[128];
    unsigned char temp_edid[128] = {0,};
    len = 0;

    for(i = 0; i < sizeof(extend_start_data); i++)
        temp_edid[len++] = extend_start_data[i];

    printf("len1 is %d\n", len);
    len += edid_constrcut_video_data(&temp_edid[len], ptr);
    printf("len2 is %d\n", len);

    for(i = 0; i < sizeof(extend_audio_data); i++)
        temp_edid[len++] = extend_audio_data[i];

    //for(i = 0; i < sizeof(extend_speaker_data);i++)
    // temp_edid[len++] = extend_speaker_data[i];
    for(i = 0; i < sizeof(extend_vendor_data); i++)
        temp_edid[len++] = extend_vendor_data[i];

    temp_edid[2] = len;
    printf("len3 is %d\n", len);
    unsigned char start_offset = ptr[2];
    unsigned char start_offset_temp = temp_edid[2];
    printf("start_offset %d start_offset_temp %d\n", start_offset, start_offset_temp);
    unsigned char num;

    for(num = 0; ((num * 18 + 18 + start_offset < 127) && (num * 18 + 18 + start_offset_temp < 127)); num++) {
        for(i = 0; i < 18; i++) {
            //printf("start_offset %d start_offset_temp %d\n",num*18 + start_offset+i,num*18 + start_offset_temp+i);
            temp_edid[start_offset_temp + num * 18 + i] =
                ptr[start_offset + num * 18 + i];
        }
    }

    printf("ok\n");
    temp_edid[127] = calculate_checksum(temp_edid);

    for(i = 0; i < 128; i++) {
        ptr[i] = temp_edid[i];
    }

    printf("over\n");
}

