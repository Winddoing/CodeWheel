unsigned char miner(unsigned char a, unsigned char b)
{
    return a < b ? a : b;
}

unsigned char calculate_checksum(unsigned char *block_beg)
{
    unsigned char sum = 0;
    unsigned char i;

    for(i = 0; i < 0x7F; i++)
        sum += block_beg[i];

    return (~sum) + 1; // Check this?!
}
